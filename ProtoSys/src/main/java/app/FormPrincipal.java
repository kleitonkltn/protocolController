    private void JtextFieldLocalMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JtextFieldLocalMouseReleased

        JFileChooser jf = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("TEXT FILES", "txt", "text");
        jf.setFileFilter(filter);

        jf.setFileSelectionMode(JFileChooser.FILES_ONLY);
        int i = jf.showSaveDialog(null);
        if (i == 1) {
            JtextFieldLocal.setText("");
        } else {
            arquivo = jf.getSelectedFile();
            JtextFieldLocal.setText(arquivo.getPath());
        }
        btCadastrar.setEnabled(true);

    }//GEN-LAST:event_JtextFieldLocalMouseReleased

